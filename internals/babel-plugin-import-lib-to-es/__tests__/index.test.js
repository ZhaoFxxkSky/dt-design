'use strict';

const { transformSync } = require('@babel/core');
const plugin = require('..');

function transform(code, options) {
  const plugins = Array.isArray(options) ? options : [[plugin, options]];
  return transformSync(code, {
    plugins,
    configFile: false,
    babelrc: false,
  }).code;
}

describe('babel-plugin-import-lib-to-es', () => {
  it('rewrites antd/lib import sources to antd/es', () => {
    const output = transform(`import { globalConfig } from 'antd/lib/config-provider';`);
    expect(output).toContain('antd/es/config-provider');
    expect(output).not.toContain('antd/lib');
  });

  it('rewrites rc-*/lib import sources', () => {
    const output = transform(`import raf from 'rc-util/lib/raf';`);
    expect(output).toContain('rc-util/es/raf');
  });

  it('rewrites @rc-component scoped packages', () => {
    const output = transform(`import x from '@rc-component/util/lib/hooks/useEvent';`);
    expect(output).toContain('@rc-component/util/es/hooks/useEvent');
  });

  it('rewrites ANY package by default, no allowlist needed', () => {
    const output = transform(`import a from 'some-new-lib/lib/a';\nconsole.log(a);`);
    expect(output).toContain('some-new-lib/es/a');
  });

  it('rewrites the bare `<pkg>/lib` entry', () => {
    const output = transform(`import x from 'antd/lib';`);
    expect(output).toContain('antd/es');
    expect(output).not.toContain('antd/lib');
  });

  it('rewrites export-from and export-all sources', () => {
    expect(transform(`export { default } from 'rc-util/lib/hooks/useEvent';`)).toContain(
      'rc-util/es/hooks/useEvent',
    );
    expect(transform(`export * from 'antd/lib/locale';`)).toContain('antd/es/locale');
  });

  it('rewrites bare require() calls', () => {
    const output = transform(`const Button = require('antd/lib/button');`);
    expect(output).toContain('antd/es/button');
    expect(output).not.toContain('antd/lib');
  });

  it('rewrites dynamic import() sources', () => {
    const output = transform(`const load = () => import('antd/lib/button');`);
    expect(output).toContain('antd/es/button');
    expect(output).not.toContain('antd/lib');
  });

  it('leaves package-root and non-lib subpath imports untouched', () => {
    const code = [
      `import { Button } from 'antd';`,
      `import merge from 'lodash/merge';`,
      `import React from 'react';`,
      `console.log(Button, merge, React);`,
    ].join('\n');
    const output = transform(code);
    expect(output).toContain('lodash/merge');
    expect(output).not.toContain('/es/');
  });

  it('supports exclude for packages without an es layout', () => {
    const code = [
      `import merge from 'lodash/lib/merge';`,
      `import { globalConfig } from 'antd/lib/config-provider';`,
      `console.log(merge, globalConfig);`,
    ].join('\n');
    const output = transform(code, { exclude: ['lodash'] });
    expect(output).toContain('lodash/lib/merge');
    expect(output).toContain('antd/es/config-provider');
  });

  it('supports regex matchers in exclude', () => {
    const output = transform(`import a from 'foo/lib/a';\nconsole.log(a);`, {
      exclude: [/^foo$/],
    });
    expect(output).toContain('foo/lib/a');
  });

  it('include switches to strict mode: only listed packages are rewritten', () => {
    const code = [`import a from 'foo/lib/a';`, `import b from 'antd/lib/b';`, `console.log(a, b);`].join(
      '\n',
    );
    const output = transform(code, { include: [/^foo$/] });
    expect(output).toContain('foo/es/a');
    expect(output).toContain('antd/lib/b');
  });

  it('rewrites imports generated by babel-plugin-import in the same pass', () => {
    const output = transform(`import { Button } from 'antd';\nconsole.log(Button);`, [
      [require.resolve('babel-plugin-import'), { libraryName: 'antd', style: false }],
      [plugin, {}],
    ]);
    expect(output).toContain('antd/es/button');
    expect(output).not.toContain('antd/lib');
  });
});
